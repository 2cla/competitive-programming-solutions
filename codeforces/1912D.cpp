void solve(){
    ll n,b;cin>>b>>n;
    vb s(n,false);
    ll tmp=1,j=1;
    while(true){
        tmp=(tmp*b)%n;
        if(tmp==1){
            cout<<2<<' '<<j<<'\n';return;
        }
        if(!tmp){
            cout<<1<<' '<<j<<'\n';return;
        }
        if(tmp==n-1){
            cout<<3<<' '<<j<<'\n';return;
        }
        if(s[tmp]){
            cout<<"0\n";return;
        }
        s[tmp]=true;j++;
    }
}
